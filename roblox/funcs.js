function fltrExps() {
  st.fltrd = expData
    .filter((exp) => {
      if (exp.hide === true) return false

      if (st.qry) {
        const q = st.qry.toLowerCase()
        const nameMtch = exp.name.toLowerCase().includes(q)
        const descMtch = exp.desc.toLowerCase().includes(q)
        const pltfMtch = exp.plat && exp.plat.some((platform) => platform.toLowerCase().includes(q))
        const uncMtch =
          exp.pros && exp.pros.some((pro) => pro.toLowerCase().includes("unc") && pro.toLowerCase().includes(q))
        const uncNtrlMtch =
          exp.neutral &&
          exp.neutral.some((ntrl) => ntrl.toLowerCase().includes("unc") && ntrl.toLowerCase().includes(q))

        if (!(nameMtch || descMtch || pltfMtch || uncMtch || uncNtrlMtch)) {
          return false
        }
      }

      if (st.pltFlt.length > 0) {
        if (!st.pltFlt.some((pltf) => exp.plat.includes(pltf))) {
          return false
        }
      }

      if (st.lvlFlt[1] !== 0 && exp.lvl !== st.lvlFlt[1]) {
        return false
      }

      if (st.lvlFlt[1] !== 0 && exp.lvl !== st.lvlFlt[1]) {
        return false
      }

      if (st.prcFlt !== "all") {
        if (st.prcFlt === "free" && exp.price !== "FREE" && !exp.free) {
          return false
        }
        if (st.prcFlt === "paid" && (exp.price === "FREE" || exp.free)) {
          return false
        }
      }

      if (st.vrfOnly && !exp.verified) {
        return false
      }

      if (st.premOnly && !exp.premium) {
        return false
      }

      if (st.extOnly) {
        const isExternal =
          (exp.pros && exp.pros.some((pro) => pro.toLowerCase().includes("external"))) ||
          (exp.neutral && exp.neutral.some((ntrl) => ntrl.toLowerCase().includes("external")))

        if (!isExternal) {
          return false
        }
      }

      if (st.execOnly) {
        const isExecutor =
          !(exp.pros && exp.pros.some((pro) => pro.toLowerCase().includes("external"))) &&
          !(exp.neutral && exp.neutral.some((ntrl) => ntrl.toLowerCase().includes("external")))

        if (!isExecutor) {
          return false
        }
      }

      if (st.keyOnly && !exp.hasKeySystem) {
        return false
      }

      if (st.noKeyOnly && exp.hasKeySystem) {
        return false
      }

      return true
    })
    .sort((a, b) => {
      const sortFn = (() => {
        switch (st.srtBy) {
          case "price-asc":
            return (a, b) => {
              const priceA = Array.isArray(a.price)
                ? Number.parseFloat(a.price[0].replace(/[^\d.]/g, "")) || 0
                : a.price === "FREE"
                  ? 0
                  : Number.parseFloat(a.price.replace(/[^\d.]/g, "")) || 0

              const priceB = Array.isArray(b.price)
                ? Number.parseFloat(b.price[0].replace(/[^\d.]/g, "")) || 0
                : b.price === "FREE"
                  ? 0
                  : Number.parseFloat(b.price.replace(/[^\d.]/g, "")) || 0

              return priceA - priceB
            }
          case "price-desc":
            return (a, b) => {
              const priceA = Array.isArray(a.price)
                ? Number.parseFloat(a.price[0].replace(/[^\d.]/g, "")) || 0
                : a.price === "FREE"
                  ? 0
                  : Number.parseFloat(a.price.replace(/[^\d.]/g, "")) || 0

              const priceB = Array.isArray(b.price)
                ? Number.parseFloat(b.price[0].replace(/[^\d.]/g, "")) || 0
                : b.price === "FREE"
                  ? 0
                  : Number.parseFloat(b.price.replace(/[^\d.]/g, "")) || 0

              return priceB - priceA
            }
          case "level-desc":
            return (a, b) => b.lvl - a.lvl
          case "name-asc":
            return (a, b) => a.name.localeCompare(b.name)
          default:
            return (a, b) => {
              if (a.verified && !b.verified) return -1
              if (!a.verified && b.verified) return 1
              if (a.premium && !b.premium) return -1
              if (!a.premium && b.premium) return 1
              return 0
            }
        }
      })()

      return sortFn(a, b)
    })

  rndrExps()
  updCnts()
}

